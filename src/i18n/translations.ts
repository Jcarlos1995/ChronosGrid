export function translate(lang: LanguageCode, key: string, params?: Record<string, string | number>): string {
  const dict = translations[lang] || translations[DEFAULT_LANGUAGE];
  let str = dict[key] ?? translations[DEFAULT_LANGUAGE][key] ?? key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    });
  }
  return str;
}

const STORAGE_KEY = 'chronosgrid-language';

function detectBrowserLanguage(): LanguageCode {
  if (typeof navigator === 'undefined') return DEFAULT_LANGUAGE;
  const browserLang = navigator.language?.split('-')[0];
  const match = languages.find((l) => l.code === browserLang);
  return match ? match.code : DEFAULT_LANGUAGE;
}

export function getStoredLanguage(): LanguageCode {
  if (typeof window === 'undefined') return DEFAULT_LANGUAGE;
  const stored = window.localStorage.getItem(STORAGE_KEY);
  if (stored && languages.some((l) => l.code === stored)) return stored as LanguageCode;
  return detectBrowserLanguage();
}

export function storeLanguage(lang: LanguageCode): void {
  if (typeof window === 'undefined') return;
  window.localStorage.setItem(STORAGE_KEY, lang);
}
